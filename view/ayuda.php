                <h3>Calificar profesores</h3>
                <p>Se califica a los profesores deacuerdo a las materias que ya se tomaron por 
                parte del alumno indicando el semestre y enseguida la materia que se tomó, una vez que
                la materia es acreditada.</p>
                <h4>Procedimiento</h4>
                <p>1. Favor de entrar a <a href="Calificar">Calificar</a></p>
                <p>2. Da click en el semetre donde se cursa la materia que quieres calificar, aparecerá un listado de materias del semestre que se eligió</p>
                <p>3. Da click en calificar a un lado de la materia que quieres calificar.</p>
                <p>4. Indica al número de veces que tomaste la materia (esto se refiere a si reprobaste en ordinario o en repetición)</p>
                <p>5. Al calificar la materia viene una lista completa de los profesores que se pueden calificar,
                es importante que busques bien el nombre y el apellido respetando acentos.</p>
                <p>6. Da una calificación, del 1-5 estrellas, al profesor</p>
                <p>7. Puedes poner una opinión si lo deseas</p>
                <p>8. Por último, <strong>da una manzana buena</strong> si crees que el profesor es la mejor opción para esa materia o
                <strong>da una manzana mala</strong> si crees que el profesor es la peor opción para esa materia</p>
                <h4>¿Qué significan las manzanas?</h4>
                <p>Dar una manzana a la calificación del profesor es importante ya que indicas a otros alumnos si el profesor
                 es una bueno o es una mala opción para tomar la materia, las manzanas son opcionales y ayudan a rankear a los profesores
                 en la plataforma, por lo que decide bien a qué profesores das manzanas</p>
                <h4>¿Otras personas podrán ver mi opinión?</h4>
                <p>Las opiniones aparecerán publicamente en las páginas de materias y profesores correspondientes, sin embargo tu opinión
                será anónima y cifrada, ¡nadie sabrá que tu escribiste eso!</p>
                <h4>El profesor con el que tomé clase no aparece en la lista</h4>
